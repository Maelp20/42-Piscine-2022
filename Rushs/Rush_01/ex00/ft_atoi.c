int	ft_atoi(int **entry, char *argv)
{
	int		x;
	int		y;
	int		z;

	x = 0;
	z = 0;
	y = 0;
	while (y <= 32)
	{
		entry[x][z] = argv[y] - 48;
		y += 2;
		z++;
		if (y == 8 || y == 16 || y == 24)
		{
			x++;
			z = 0;
		}
	}
	return (1);
}
