#include <unistd.h>

int	main(int ac, char **av)
{
	int	temp[127];
	int	i;
	int	j;

	if (ac == 3)
	{
		i = 0;
		while (i < 127)
		{
			temp[i] = 0;
			i++;
		}
		i = 0;
		while (av[1][i])
		{
			j = 0;
			while(av[2][j])
			{
				if (temp[av[1][i]] == 0 && av[1][i] == av[2][j])
				{
					write (1, &av[1][i], 1);
					temp[av[1][i]] = 1;
				}
				j++;
			}
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
