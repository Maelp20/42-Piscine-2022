int	ft_sqrt(int nb)
{
	int		n;
	int		r;

	n = nb;
	r = 1;
	if (n == 0)
		return (0);
	while ((r * r) < n && r < 46341)
		r++;
	if ((r * r) == n)
		return (r);
	else
		return (0);
}
