#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int	i;

	if (nb < 0 && nb > -2147483648)
	{
		ft_putchar('-');
		i = -nb;
	}
	else
		i = nb;
	if (i > 9)
	{
		ft_putnbr(i / 10);
		i = i % 10;
	}
	if (nb == -2147483648)
	{
		write (1, "-2147483648", 11);
	}
	else
		ft_putchar('0' + i);
}
